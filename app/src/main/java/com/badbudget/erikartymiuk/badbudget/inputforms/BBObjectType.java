/**
 * Enumeration defining all the valid types of BBObjects that can be used for Adding, Editing,
 * and Deleting task operations.
 *
 * Created by Erik Artymiuk on 7/27/2016.
 */
public enum BBObjectType {
    ACCOUNT, ACCOUNT_SAVINGSACCOUNT,
    DEBT, DEBT_CREDITCARD, DEBT_LOAN, DEBT_MISC,
    GAIN, LOSS,
    BUDGETITEM
}
